INSERT INTO users (nombre)
SELECT 
    'Nombre' || id
FROM generate_series(1, 3000) AS id;

select * from users;

select id, 
	UPPER(nombre) as Upper_name,
	LOWER(nombre) as Lower_name,
	LENGTH(nombre) as Lenght,
	(20 * LENGTH(nombre) ) as constante,
	'*' || id || '-' || nombre  as barcode,
	CONCAT('*', id,'-', nombre) as Concatenar_id,
	nombre

from users;


